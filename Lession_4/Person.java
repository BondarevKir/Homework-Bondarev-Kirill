package Lession_4;

public class Person {
    public String name;
    public String position;
    public String email;
    public long phoneNumber;
    public int salary;
    public int age;

    public Person(String name, String position, String email, long phoneNumber, int salary, int age) {
        this.name = name;
        this.email = email;
        this.position = position;
        this.phoneNumber = phoneNumber;
        this.salary = salary;
        this.age = age;
    }
}
